     public void centrarVentanaI(JInternalFrame jfcentrar){
        int x1 = (jDeskEgresos.getWidth()/2)- jfcentrar.getWidth()/2;
        int y1 = (jDeskEgresos.getHeight()/2)- jfcentrar.getHeight()/2;
        if(jfcentrar.isShowing()){
            jfcentrar.setLocation(x1, y1);
        }else{
            jDeskEgresos.add(jfcentrar);
            jfcentrar.setLocation(x1, y1);
            jfcentrar.show();
        }
    }
    
    

